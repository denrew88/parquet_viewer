def nulls_last(value: Any) -> tuple[int, Any]:
    return (1, 0) if value is None else (0, value)


def make_boundary_query_map(root: Path) -> dict[str, Any]:
    path = root / "small" / "boundary-query-map.parquet"
    rows = list(range(200))
    # A full permutation gives scattered but unique source identities.
    source_ids = [(row * 73 + 19) % len(rows) for row in rows]
    included = [row % 3 != 0 for row in rows]
    sort_a = [None if row % 17 == 0 else row % 7 for row in rows]
    sort_b = [None if row % 19 == 0 else chr(ord("A") + row % 5) for row in rows]
    sort_c = [row % 4 for row in rows]
    table = pa.table(
        {
            "source_row_id": pa.array(source_ids, type=pa.int64()),
            "included": pa.array(included),
            "sort_a": pa.array(sort_a, type=pa.int32()),
            "sort_b": pa.array(sort_b),
            "sort_c": pa.array(sort_c, type=pa.int16()),
            "value": pa.array([None if row % 23 == 0 else ("" if row % 29 == 0 else f"v-{row % 11}") for row in rows]),
        }
    )
    write_parquet(path, table)
    filtered = [index for index in rows if included[index]]
    sorted_all = sorted(
        rows,
        key=lambda index: (
            nulls_last(sort_a[index]),
            nulls_last(sort_b[index]),
            -sort_c[index],
            source_ids[index],
        ),
    )
    filtered_sorted = [index for index in sorted_all if included[index]]
    return parquet_entry(
        "boundary-query-map",
        path,
        "filter-stable-three-sort",
        {
            "filterOnlySourceRowIds": [source_ids[index] for index in filtered],
            "sortOnlySourceRowIds": [source_ids[index] for index in sorted_all],
            "filterThenSortSourceRowIds": [source_ids[index] for index in filtered_sorted],
            "sort": ["sort_a ASC NULLS LAST", "sort_b ASC NULLS LAST", "sort_c DESC NULLS LAST", "source_row_id ASC"],
        },
    )


