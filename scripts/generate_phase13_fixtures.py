def utc_now() -> str:
    return datetime.now(timezone.utc).isoformat()


def sha256_file(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as stream:
        for chunk in iter(lambda: stream.read(8 * 1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def schema_json(schema: pa.Schema) -> list[dict[str, Any]]:
    return [
        {"name": field.name, "type": str(field.type), "nullable": field.nullable}
        for field in schema
    ]


def schema_sha256(schema: pa.Schema) -> str:
    return hashlib.sha256(schema.serialize().to_pybytes()).hexdigest()


def stable_u64(value: int, salt: int = 0) -> int:
    mixed = (value + SEED + salt) & 0xFFFF_FFFF_FFFF_FFFF
    mixed ^= mixed >> 30
    mixed = (mixed * 0xBF58_476D_1CE4_E5B9) & 0xFFFF_FFFF_FFFF_FFFF
    mixed ^= mixed >> 27
    mixed = (mixed * 0x94D0_49BB_1331_11EB) & 0xFFFF_FFFF_FFFF_FFFF
    mixed ^= mixed >> 31
    return mixed


def stable_u64_array(values: np.ndarray, salt: int = 0) -> np.ndarray:
    """Vectorized splitmix64 with the same wrapping semantics as stable_u64."""
    mixed = values.astype(np.uint64, copy=True)
    mixed += np.uint64(SEED + salt)
    mixed ^= mixed >> np.uint64(30)
    mixed *= np.uint64(0xBF58_476D_1CE4_E5B9)
    mixed ^= mixed >> np.uint64(27)
    mixed *= np.uint64(0x94D0_49BB_1331_11EB)
    mixed ^= mixed >> np.uint64(31)
    return mixed


def write_bytes(path: Path, payload: bytes) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    partial = path.with_suffix(path.suffix + ".partial")
    partial.write_bytes(payload)
    partial.replace(path)


def write_text(path: Path, text: str) -> None:
    write_bytes(path, text.encode("utf-8"))


def write_parquet(path: Path, table: pa.Table, row_group_size: int | None = None) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    partial = path.with_suffix(path.suffix + ".partial")
    partial.unlink(missing_ok=True)
    pq.write_table(
        table,
        partial,
        compression="zstd",
        compression_level=3,
        use_dictionary=True,
        row_group_size=row_group_size or max(1, table.num_rows),
        data_page_version="2.0",
    )
    partial.replace(path)


def parquet_entry(
    fixture_id: str,
    path: Path,
    profile: str,
    oracle: dict[str, Any],
) -> dict[str, Any]:
    parquet = pq.ParquetFile(path)
    schema = parquet.schema_arrow
    return {
        "id": fixture_id,
        "kind": "parquet",
        "path": path.as_posix(),
        "profile": profile,
        "rows": parquet.metadata.num_rows,
        "columns": parquet.metadata.num_columns,
        "rowGroups": parquet.metadata.num_row_groups,
        "schema": schema_json(schema),
        "schemaSha256": schema_sha256(schema),
        "bytes": path.stat().st_size,
        "sha256": sha256_file(path),
        "oracle": oracle,
        "audit": "PASS",
    }


def file_entry(
    fixture_id: str,
    path: Path,
    profile: str,
    rows: int | None,
    columns: int | None,
    oracle: dict[str, Any],
    *,
    kind: str = "csv",
    schema: list[dict[str, Any]] | None = None,
) -> dict[str, Any]:
    return {
        "id": fixture_id,
        "kind": kind,
        "path": path.as_posix(),
        "profile": profile,
        "rows": rows,
        "columns": columns,
        "schema": schema,
        "bytes": path.stat().st_size,
        "sha256": sha256_file(path),
        "oracle": oracle,
        "audit": "PASS",
    }


