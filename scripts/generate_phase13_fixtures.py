LARGE_SCHEMA = pa.schema(
    [
        pa.field("row_id", pa.int64(), nullable=False),
        pa.field("group_id", pa.int64(), nullable=False),
        pa.field("category", pa.string(), nullable=False),
        pa.field("active", pa.bool_(), nullable=False),
        pa.field("optional_value", pa.int32(), nullable=True),
        pa.field("event_time", pa.timestamp("ns", tz="UTC"), nullable=False),
        pa.field("amount", pa.float64(), nullable=False),
        pa.field("label", pa.string(), nullable=False),
        pa.field("code", pa.string(), nullable=False),
        *[pa.field(f"metric_{index:02d}", pa.int64(), nullable=False) for index in range(6)],
    ]
)


def large_columns(start: int, count: int, cardinality: str) -> dict[str, Iterable[Any]]:
    indices = np.arange(start, start + count, dtype=np.int64)
    mixed = stable_u64_array(indices, 101)
    if cardinality == "low":
        group = indices % 128
        category = [f"category-{value:02d}" for value in indices % 16]
        label = [f"label-{value:03d}" for value in indices % 256]
        code = [f"code-{value:02d}" for value in indices % 64]
    else:
        group = (mixed & np.uint64(0x7FFF_FFFF_FFFF_FFFF)).astype(np.int64)
        category = [f"category-{int(value):016x}" for value in mixed]
        labels = stable_u64_array(indices, 211)
        codes = stable_u64_array(indices, 307)
        label = [f"label-{int(value):016x}" for value in labels]
        code = [f"code-{int(value):016x}" for value in codes]
    optional = (indices % 10_000).astype(np.int32)
    return {
        "row_id": indices,
        "group_id": group,
        "category": category,
        "active": indices % 2 == 0,
        "optional_value": pa.array(optional, mask=indices % 97 == 0, type=pa.int32()),
        "event_time": pa.array(1_700_000_000_000_000_000 + indices * 1_000_003, type=pa.timestamp("ns", tz="UTC")),
        "amount": (mixed % np.uint64(10_000_000)).astype(np.float64) / 100.0,
        "label": label,
        "code": code,
        **{f"metric_{column:02d}": (indices * (column + 3) + column * 17) % (10_000 + column * 101) for column in range(6)},
    }


def large_batch(start: int, count: int, cardinality: str) -> pa.RecordBatch:
    columns = large_columns(start, count, cardinality)
    arrays = [pa.array(columns[field.name], type=field.type) for field in LARGE_SCHEMA]
    return pa.RecordBatch.from_arrays(arrays, schema=LARGE_SCHEMA)


def write_large_csv(path: Path, cardinality: str, malformed_last_row: bool) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    partial = path.with_suffix(path.suffix + ".partial")
    with partial.open("w", encoding="utf-8", newline="", buffering=8 * 1024 * 1024) as stream:
        stream.write(",".join(LARGE_SCHEMA.names) + "\n")
        for start in range(0, LARGE_ROWS, LARGE_BATCH_ROWS):
            count = min(LARGE_BATCH_ROWS, LARGE_ROWS - start)
            columns = large_columns(start, count, cardinality)
            lines: list[str] = []
            for offset in range(count):
                row = start + offset
                optional = "" if row % 97 == 0 else str(row % 10_000)
                label = str(columns["label"][offset])
                if malformed_last_row and row == LARGE_ROWS - 1:
                    label = '"' + ("x" * 1_048_577)
                values = [
                    str(row),
                    str(columns["group_id"][offset]),
                    str(columns["category"][offset]),
                    "true" if row % 2 == 0 else "false",
                    optional,
                    str(1_700_000_000_000_000_000 + row * 1_000_003),
                    f"{float(columns['amount'][offset]):.2f}",
                    label,
                    str(columns["code"][offset]),
                    *[str(int(columns[f"metric_{column:02d}"][offset])) for column in range(6)],
                ]
                lines.append(",".join(values))
            stream.write("\n".join(lines) + "\n")
            print(f"[{path.name}] {start + count:,}/{LARGE_ROWS:,}", file=sys.stderr, flush=True)
    partial.replace(path)


def write_large_parquet(path: Path, cardinality: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    partial = path.with_suffix(path.suffix + ".partial")
    partial.unlink(missing_ok=True)
    writer = pq.ParquetWriter(partial, LARGE_SCHEMA, compression="zstd", compression_level=3, use_dictionary=True, data_page_version="2.0")
    try:
        for start in range(0, LARGE_ROWS, PARQUET_ROW_GROUP_ROWS):
            count = min(PARQUET_ROW_GROUP_ROWS, LARGE_ROWS - start)
            writer.write_batch(large_batch(start, count, cardinality), row_group_size=count)
            print(f"[{path.name}] {start + count:,}/{LARGE_ROWS:,}", file=sys.stderr, flush=True)
    finally:
        writer.close()
    partial.replace(path)


def make_large(root: Path) -> list[dict[str, Any]]:
    entries: list[dict[str, Any]] = []
    for cardinality in ("low", "high"):
        csv_path = root / "large" / f"csv-{LARGE_ROWS}-{cardinality}.csv"
        if not csv_path.exists():
            write_large_csv(csv_path, cardinality, False)
        entries.append(file_entry(f"csv-{LARGE_ROWS}-{cardinality}", csv_path, f"{cardinality}-cardinality-15-column", LARGE_ROWS, LARGE_COLUMNS, {"rowIdRange": [0, LARGE_ROWS - 1], "malformedRows": []}, schema=schema_json(LARGE_SCHEMA)))
        parquet_path = root / "large" / f"boundary-{LARGE_ROWS}-{cardinality}.parquet"
        if not parquet_path.exists():
            write_large_parquet(parquet_path, cardinality)
        entries.append(parquet_entry(f"boundary-{LARGE_ROWS}-{cardinality}", parquet_path, f"{cardinality}-cardinality-15-column", {"rowIdRange": [0, LARGE_ROWS - 1], "rowFunction": "stable_u64(index, salt)"}))
    invalid_path = root / "large" / f"csv-{LARGE_ROWS}-long-invalid.csv"
    if not invalid_path.exists():
        write_large_csv(invalid_path, "low", True)
    entries.append(file_entry(f"csv-{LARGE_ROWS}-long-invalid", invalid_path, "low-cardinality-final-long-malformed-row", LARGE_ROWS, LARGE_COLUMNS, {"malformedRows": [LARGE_ROWS - 1], "malformedPayloadBytes": 1_048_577, "error": "unclosed quote at final logical row"}, schema=schema_json(LARGE_SCHEMA)))
    return entries


def ensure_large_path_is_safe(workspace: Path, output: Path) -> None:
    relative = output.resolve().relative_to(workspace.resolve())
    if len(relative.parts) < 2 or relative.parts[0] != ".tmp" or not relative.parts[1].startswith("phase13-"):
        raise ValueError("--large output must be below .tmp/phase13-*")


def clean_owned_outputs(root: Path) -> None:
    if not root.exists():
        return
    for child in root.iterdir():
        if child.name in {"small", "large"}:
            shutil.rmtree(child)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument("--manifest", type=Path, default=DEFAULT_MANIFEST)
    parser.add_argument("--large", action="store_true", help="also generate the 5,850,000-row fixtures")
    parser.add_argument("--clean", action="store_true", help="remove only this generator's small/large output directories first")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    workspace = Path(__file__).resolve().parents[1]
    output = (workspace / args.output_dir).resolve() if not args.output_dir.is_absolute() else args.output_dir.resolve()
    manifest_path = (workspace / args.manifest).resolve() if not args.manifest.is_absolute() else args.manifest.resolve()
    if args.large:
        ensure_large_path_is_safe(workspace, output)
    output.mkdir(parents=True, exist_ok=True)
    if args.clean:
        clean_owned_outputs(output)

    fixtures: list[dict[str, Any]] = []
    fixtures.append(make_boundary_states(output))
    fixtures.append(make_boundary_query_map(output))
    fixtures.append(make_boundary_long_string(output))
    fixtures.extend(make_csv_matrix(output))
    fixtures.extend(make_profile_csv(output))
    fixtures.extend(make_duration_parquet(output))
    fixtures.extend(make_timestamp_and_wide(output))
    if args.large:
        fixtures.extend(make_large(output))

    generator_path = Path(__file__).resolve()
    manifest = {
        "schemaVersion": 1,
        "generatorVersion": GENERATOR_VERSION,
        "generator": generator_path.as_posix(),
        "generatorSha256": sha256_file(generator_path),
        "seed": SEED,
        "generatedAtUtc": utc_now(),
        "outputDirectory": output.as_posix(),
        "largeIncluded": args.large,
        "environment": {"python": platform.python_version(), "numpy": np.__version__, "pyarrow": pa.__version__, "platform": platform.platform()},
        "configuration": {"largeRows": LARGE_ROWS, "largeColumns": LARGE_COLUMNS, "largeBatchRows": LARGE_BATCH_ROWS, "parquetRowGroupRows": PARQUET_ROW_GROUP_ROWS, "compression": "zstd"},
        "fixtures": fixtures,
        "summary": {"fixtureCount": len(fixtures), "bytes": sum(entry["bytes"] for entry in fixtures), "allAuditsPass": all(entry["audit"] == "PASS" for entry in fixtures)},
        "validation": "PASS",
    }
    manifest_path.parent.mkdir(parents=True, exist_ok=True)
    manifest_path.write_text(json.dumps(manifest, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(json.dumps({"manifest": manifest_path.as_posix(), "fixtures": len(fixtures), "bytes": manifest["summary"]["bytes"], "large": args.large}))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
